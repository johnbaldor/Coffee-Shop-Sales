/*
void total_sales(int rows, int cols, int data[][cols])
Prints the total sales throughout the year of each item to a output file  

Parameters:
    rows: number of rows in data 
    cols: number of columns in data
    data: array of sales data  
Returns: 
    total sales of each item  
*/
void total_sales(int rows, int cols, int data[][cols]);



