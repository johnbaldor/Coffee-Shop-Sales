
/*
void print_item(int rows, int cols, int data[][cols])
Prints the sales for each month of a user requested item to the terminal 

Parameters:
    rows: number of rows in data 
    cols: number of columns in data
    data: array of sales data  
Returns: 
    sales of requested item each month 
*/
